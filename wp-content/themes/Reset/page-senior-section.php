						<div class="text">
							The stamps on our virtual passports sealed the deal. We were no longer elementary school kids waiting anxiously to hear from secondary schools — we were Harvard-Westlake students. The rest of sixth grade passed in a blur of graduations and nervously interacting with our future classmates at that incredibly awkward pool party. In the fall, the transition to the North Faring campus proved somewhat difficult for most. We all felt like freaks, geeks, outsiders and newbies, and these feelings were only intensified by the much older and wiser eighth graders, who shouted "sevvies" at us as we ran to our classes (and attempted to navigate the convoluted maze that was Reynolds Hall). We were all trying to fit in, whether that meant finding a table on the terrace or sitting next to a potential friend in pre-algebra. Much like Sam Weir, we were new to the scene with millions of questions: what am I supposed to do with a free period? Do I actually have to go on retreat while it’s pouring rain? Am I supposed to grind at Young Angels/Bar and Bat Mitzvahs? Do I have to wear that embarrassing neon yellow vest for during-school SQUID and after-school SQUID? Is that sushi in the cafeteria? Our futures at Harvard-Westlake completely depended on whom we sat next to on the bus or partnered up with in P.E., or at least that’s how it seemed.
						</div>
