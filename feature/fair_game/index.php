				<div>	
					<p>However, the varsity girls’ soccer team ran into trouble last year over its methods of promotion.  A player urged students to attend a girls’ soccer game in a Facebook post that included the term “soccer sluts,” in what teammates say was a tongue-in-cheek parody of similarly offensive online party invitations.</p>
					<p>Some students who viewed the post, however, didn’t consider it humorous.</p>
					<p>“There were posts before that of a similar subject matter, along the lines of ‘come to our game because we’re hot,’ but I felt like this one was really crossing a line,” Ethan Weinstein ’15, who commented on the post, said. “They didn’t even mention that the girls’ soccer team was the number one team in the nation. It just seemed like their entire existence as a Harvard-Westlake identity was contingent upon the fact that they were hot girls wearing short shorts.”</p>
					<p>Milly said she wasn’t offended by the post because it was meant as a joke, though she thought some of her teammates may have felt differently.</p>				
				</div>
			</div>
