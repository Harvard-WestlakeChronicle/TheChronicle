		<section id="story-intel" class="story-intel" style="z-index: -1;">
			<div class="story btext">
				<p class="first"><span id="drop">W</span>hen Sandra* ’15 slides on her kneepads and strides onto the volleyball court, she isn’t there to play nice.  As a member of a volleyball team that was CIF runner-up last year, she and her teammates are among the top players in Southern California. Their games are also among the school’s most popular, drawing crowds of hundreds of chanting, face-painted fans.</p>
				<p>how does she feel when students casually reference the appeal of players’ teensy spandex shorts? Sandra says it doesn’t bother her.</p>
				<p>“That’s just a joke,” she said. “I think because the volleyball girls are comfortable with it, it’s not really disrespectful.”</p>
				<p>Sandra’s statement is part of a larger debate over the treatment of female athletes at Harvard-Westlake.</p>
				<p>According to a Chronicle poll of 421 students, 65 percent believe that female athletes are treated differently from male athletes by students, while 45 percent think they are treated differently by the administration.</p>
				<p>The issue has been an especially hot topic in recent weeks, after an assistant football coach made an offhand comment in which he compared his players’ poor tackling ability to that of the girls’ field hockey team while they were within earshot. The football coaches and Head of Athletics Terry Barnum later apologized, and Barnum also spoke to both about the importance of respecting female athletes.</p> 
			</div>
		</section>


		<section id="story-drop" class="story-drop">
			<div class="story wtext">
				<blockquote class="blockq right">
					<p>“My message to them was… all athletes male and female are to be respected and to be treated fairly, no matter what sport they play and no matter what gender they are,” Barnum said.</p>
				</blockquote>
			</div>
		</section>


		<section id="story-intel" class="story-intel">
			<div class="story btext">
				<div>
					<p>Yet some students say the problem is not a single coach’s remark, but the larger attitudes they believe it reflects.</p>
					<p>“There’s definitely more of an appreciation and respect for the guys’ sports,” varsity track and field athlete Daisy ’15 said.</p>
					<p>Barnum said the Athletics Department was looking into ways to encourage education among students about the importance of female athletics, possibly through a video or discussion at a First and Third Wednesday assembly. He did not, however, think that there was any sort of discrimination.</p>
					<p>“The girls on the field hockey team felt like female athletes are treated by their peers or viewed by their peers as worse than male athletes. I'm not sure if I completely agree with that,” Barnum said. “I think in some instances that might be true. I'm not saying it's patently false. At the same time I think there are instances- our girls volleyball team, our girls soccer team, our girls track and field team- those are three instances, of girls that are held in very high regard by their peers, male and female alike, and feel very respected for the work they do and the work they've accomplished.”</p>
					<p>Several members of the girls’ varsity soccer team, including Milly* ’15 agreed they had been treated with nothing but respect by students and administrators.</p>
				</div>
			</div>
		</section>
		
		<section id="story-drop" class="story-drop">
			<div class="story wtext">
				<blockquote class="blockq left">
					<p>“Though I’ve never experienced anything bad, it could be different across different sports, depending on the culture around the team,” Milly said. “Field hockey, for example, doesn’t have a boys’ equivalent, and there’s definitely a negative, girly stigma around it.”</p>
				</blockquote>
			</div>
