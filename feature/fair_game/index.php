			<div class="story btext">
				<div>
					<p>Soccer coach Richard Simms said such comments were absolutely unacceptable.</p>
					<p>“It happens, and it's something I've been very serious about trying to discourage kids from doing,” Simms said. “I've been punitive about it in the past when I know our kids have done it and I've found out about it... I would say societal culture uses sex to sell things and promote things. At our school that's something we should discourage, and celebrate our girls as the athletes they are.”</p>
					<p>While Sandra felt that comments like those made by the assistant football coach were inappropriate, she didn’t mind remarks about revealing spandex.</p>
					<p>“I’m confident enough in myself and my own [athleticism] that it doesn’t bother me,” she said. “And if more people come [to games], great…  Some people can just get too politically correct.”</p>
					<p>The athletic department discourages students from promoting games through objectification.</p>
					<p>“It’s unfortunate,” Athletic Director Darlene Bible said. “That's trying to find ways to get people to come to their contests in the wrong manner.”</p>
					<p>Student Athletic Advisory Committee member Jeremy Tepper ’15 didn’t believe such remarks were indicative of a larger issue.</p>
					<p>“I would say that comments on Facebook groups by a specific person or any one person’s comments in general don’t reflect the student body and school as a whole,” said Tepper, who plays varsity football and volleyball.</p>
					<p>Head of School Jeanne Huybrechts agreed, adding that remarks like the assistant football coach’s were rare.</p>
					<p>“I could probably count on one hand the number of times that there has been friction between any two teams at this school, and by that I mean girls and boys or boys and boys,” Huybrechts said. “It just doesn’t happen very often.”</p>
					<p>Field hockey player Jessica Spitz ’15 disagrees, saying that comments like the football coach’s should not be ignored.</p>
				</div>
			</div>
		</section>
		
