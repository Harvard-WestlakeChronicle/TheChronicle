			<div class="story wtext">
				<blockquote class="blockq right">
					<p>“It's a lot easier to categorize this as an isolated incident rather than face the problem head on,” Spitz said. “The fact that this kind of behavior goes on at all is cause for concern, and writing it off as not reflective of the school's athletic culture is a mistake.”</p>
				</blockquote>
			</div>
		</section>
		
		<section id="story-intel" class="story-intel end">
			<div class="story btext">
				<div>
					<p>Spitz also wrote an opinion article criticizing the remark and the ethos she felt it reflected. The piece appeared in the fall issue of the school sports magazine Big Red and the Chronicle Online, and was widely circulated on social media, receiving over 3,500 page views.</p>
					<p>Some, however, felt that the article blew the issue out of proportion.</p>
					<p>“It wasn't even the football team saying it... it was one coach,” varsity football player Alec Dominick ’15 said. “I obviously think it's wrong, but it's not like he was even saying it to their face...obviously since the field hockey team doesn't play football they would tackle worse than us.”</p>
					<p>Other students say that the issue is not so much the comments about female athletes as it is the turnout at their games.</p>
					<p>Although volleyball is widely attended by student fans, many other girls’ sports are not.</p>
					<p>“Let’s be honest- would you rather watch girls basketball or boys?” Franklin* ’16 asked. “Obviously boys. The game’s just more exciting, and girls can’t dunk like in boys’ basketball.”</p>
					<p>Varsity track athlete Daisy says she tries not to let any gender inequity distract her from performing her best.</p>
					<p>“Guys sports have traditionally been more valued everywhere,” Daisy said. “Harvard-Westlake is just a little part of that. It’s getting better, and we’re probably a lot better than most high schools, but it’s still an issue.”</p>
					<p>*Names have been changed</p>
